//================================================================================================//
// События DOM - взаимодействие пользователя с элементами страницы.

// Событие — это действие, которое происходит в браузере и может быть обработано JavaScript. 
// Например, клик мыши, нажатие клавиши или загрузка страницы — всё это события.

// Представьте себе, что DOM — это сцена, а события — это действия актёров. 
// Клик по кнопке — как поднятая рука на собрании, а `DOMContentLoaded` — как поднятие занавеса перед началом спектакля.

//================================================================================================//
// События мыши

/* 
    click — происходит при клике на элемент.
    contextmenu — вызывается при нажатии правой кнопки мыши (контекстное меню).
    mouseover / mouseout — когда мышь входит или выходит из элемента.
    mousedown / mouseup — когда нажата и отпущена кнопка мыши.
    mousemove — отслеживает движение курсора.
*/

// Пример:
document.querySelector('button').addEventListener('click', () => {
    alert('Кнопка нажата!');
});

// Аналогия: Мышь — как кисть художника, двигаясь по холсту (странице), она вызывает различные реакции.
//================================================================================================//

// События элементов управления

/* 
    focus — возникает, когда элемент получает фокус (например, поле ввода).
    submit — срабатывает при отправке формы.
*/

// Пример:
document.querySelector('input').addEventListener('focus', () => {
    console.log('Фокус на поле ввода');
});

// Аналогия: Фокус похож на свет прожектора, освещающий определённую область на сцене.
//================================================================================================//

// События клавиатуры

/* 
    keyup — происходит при отпускании клавиши.
    keydown — происходит при нажатии клавиши.
*/

// Пример:
document.addEventListener('keydown', (event) => {
    console.log(`Нажата клавиша: ${event.key}`);
});

// Аналогия: Это похоже на игру на пианино — каждая нажатая клавиша вызывает звук (или реакцию в браузере).
//================================================================================================//

// События CSS

/* 
    transitionend — срабатывает после завершения CSS-анимации.
*/

// Пример:
document.querySelector('.box').addEventListener('transitionend', () => {
    console.log('Анимация завершена!');
});

// Аналогия: Представьте сцену в театре, которая плавно сменяется из одного декора в другой.
//================================================================================================//

// События документа

/* 
    DOMContentLoaded — когда HTML загружен и DOM построен.
*/

// Пример:
document.addEventListener('DOMContentLoaded', () => {
    console.log('Страница загружена!');
});

// Аналогия: Это как открыть книгу — страницы уже на месте, можно читать.
//================================================================================================//

// Всплытие / погружение событий

/*
    1. Фаза погружения (capturing) — событие проходит сверху вниз.
    2. Фаза цели (target) — событие доходит до целевого элемента.
    3. Фаза всплытия (bubbling) — событие поднимается обратно вверх.
*/

// Пример:
document.querySelector('.parent').addEventListener('click', () => {
    console.log('Событие сработало на родителе');
}, true); // true означает фазу погружения

document.querySelector('.child').addEventListener('click', () => {
    console.log('Событие сработало на дочернем элементе');
});

// Аналогия: Представьте камень, брошенный в воду: сначала он погружается, потом достигает дна (цели), 
// а затем волны всплывают обратно на поверхность.

